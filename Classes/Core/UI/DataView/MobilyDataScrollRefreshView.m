#import "MobilyDataScrollView.h"

/*--------------------------------------------------*/

@interface MobilyDataScrollRefreshView ()



@end

/*--------------------------------------------------*/
#pragma mark -
/*--------------------------------------------------*/

@implementation MobilyDataScrollRefreshView

#pragma mark Synthesize

@synthesize objectName = _objectName;
@synthesize objectParent = _objectParent;
@synthesize objectChilds = _objectChilds;
@synthesize widget = _widget;
@synthesize state = _state;

#pragma mark NSKeyValueCoding

#pragma mark Init / Free

- (instancetype)initWithCoder:(NSCoder*)coder {
    self = [super initWithCoder:coder];
    if(self != nil) {
        [self setup];
    }
    return self;
}

- (instancetype)initWithFrame:(CGRect)frame {
    self = [super initWithFrame:frame];
    if(self != nil) {
        [self setup];
    }
    return self;
}

- (void)setup {
    self.backgroundColor = [UIColor redColor];
}

- (void)dealloc {
    self.objectName = nil;
    self.objectParent = nil;
    self.objectChilds = nil;
}

#pragma mark MobilyBuilderObject

- (NSArray*)relatedObjects {
    return [_objectChilds relativeComplement:self.subviews];
}

- (void)addObjectChild:(id< MobilyBuilderObject >)objectChild {
    if([objectChild isKindOfClass:UIView.class] == YES) {
        self.objectChilds = [NSArray arrayWithArray:_objectChilds andAddingObject:objectChild];
    }
}

- (void)removeObjectChild:(id< MobilyBuilderObject >)objectChild {
    if([objectChild isKindOfClass:UIView.class] == YES) {
        self.objectChilds = [NSArray arrayWithArray:_objectChilds andRemovingObject:objectChild];
    }
}

- (void)willLoadObjectChilds {
}

- (void)didLoadObjectChilds {
}

- (id< MobilyBuilderObject >)objectForName:(NSString*)name {
    return [MobilyBuilderForm object:self forName:name];
}

- (id< MobilyBuilderObject >)objectForSelector:(SEL)selector {
    return [MobilyBuilderForm object:self forSelector:selector];
}

#pragma mark UIView



#pragma mark Property

- (void)setState:(MobilyDataScrollRefreshViewState)state {
    if(_state != state) {
        _state = state;
        
        switch(_state) {
            case MobilyDataScrollRefreshViewStateIdle: [self didIdle]; break;
            case MobilyDataScrollRefreshViewStatePull: [self didPull]; break;
            case MobilyDataScrollRefreshViewStateRelease: [self didRelease]; break;
            case MobilyDataScrollRefreshViewStateLoading: [self didLoading]; break;
        }
    }
}

#pragma mark Public

- (void)didIdle {
}

- (void)didPull {
}

- (void)didRelease {
}

- (void)didLoading {
}

@end

